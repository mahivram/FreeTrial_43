export const getYouTubeSearchQuery = (skill, level = 'beginner', languageCode = 'hi') => {
  const skillData = skillTutorialsData[skill];
  
  // Map language codes to query languages
  const languageMap = {
    'hi': 'hindi',
    'en': 'english',
    'mr': 'marathi',
    'gu': 'gujarati',
    'ta': 'tamil'
  };

  const queryLanguage = languageMap[languageCode] || 'hindi';

  if (!skillData) {
    console.warn(`No data found for skill: ${skill}. Using generic search query.`);
    // Use language-specific template for generic queries
    const template = queryTemplates[queryLanguage];
    return template ? template.basic.replace('{skill}', skill) : `${skill} tutorial`;
  }

  // Try to get language specific queries, fallback to default language
  const languageQueries = skillData.searchQueries[queryLanguage] || 
                         skillData.searchQueries[skillData.searchQueries.defaultLanguage];
  
  if (!languageQueries) {
    console.warn(`No queries found for language: ${queryLanguage} in skill: ${skill}`);
    // Use language-specific template as fallback
    const template = queryTemplates[queryLanguage];
    const query = template ? template.tutorial.replace('{skill}', skill) : `${skill} tutorial`;
    return level === 'beginner' ? query : `advanced ${query}`;
  }

  const query = languageQueries[level] || languageQueries.beginner;
  console.log('Generated query for', skill, level, queryLanguage, ':', query);
  return query;
};

